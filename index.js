// server/routes.ts
import { z } from "zod";
var uploadDir = path.join(process.cwd(), "uploads");
if (!fs.existsSync(uploadDir)) {
  fs.mkdirSync(uploadDir, { recursive: true });
}
var multerStorage = multer.diskStorage({
  destination: (req, file, cb) => {
    cb(null, uploadDir);
  },
  filename: (req, file, cb) => {
    const uniqueSuffix = Date.now() + "-" + Math.round(Math.random() * 1e9);
    cb(null, file.fieldname + "-" + uniqueSuffix + path.extname(file.originalname));
  }
});
var upload = multer({
  storage: multerStorage,
  limits: {
    fileSize: 5 * 1024 * 1024
    // 5MB limit
  },
  fileFilter: (req, file, cb) => {
    const allowedMimeTypes = ["image/jpeg", "image/png", "image/gif", "image/webp"];
    if (allowedMimeTypes.includes(file.mimetype)) {
      cb(null, true);
    } else {
      cb(new Error("Only image files are allowed"));
    }
  }
});
async function registerRoutes(app2) {
  app2.use("/uploads", (req, res, next) => {
    res.header("Cross-Origin-Resource-Policy", "cross-origin");
    next();
  }, express.static(uploadDir));
  app2.post("/api/upload-profile-image", upload.single("image"), async (req, res) => {
    try {
      if (!req.file) {
        return res.status(400).json({ message: "No file uploaded" });
      }
      const imageData = {
        filename: req.file.filename,
        originalName: req.file.originalname,
        mimeType: req.file.mimetype,
        size: req.file.size.toString()
      };
      const validatedData = insertProfileImageSchema.parse(imageData);
      const profileImage = await storage.createProfileImage(validatedData);
      res.json({
        id: profileImage.id,
        url: `/uploads/${profileImage.filename}`,
        originalName: profileImage.originalName
      });
    } catch (error) {
      if (error instanceof z.ZodError) {
        return res.status(400).json({ message: "Invalid image data", errors: error.errors });
      }
      console.error("Upload error:", error);
      res.status(500).json({ message: "Failed to upload image" });
    }
  });
  app2.get("/api/profile-image", async (req, res) => {
    try {
      const profileImage = await storage.getLatestProfileImage();
      if (!profileImage) {
        return res.status(404).json({ message: "No profile image found" });
      }
      res.json({
        id: profileImage.id,
        url: `/uploads/${profileImage.filename}`,
        originalName: profileImage.originalName
      });
    } catch (error) {
      console.error("Get profile image error:", error);
      res.status(500).json({ message: "Failed to get profile image" });
    }
  });
  app2.post("/api/contact", async (req, res) => {
    try {
      const validatedData = insertContactMessageSchema.parse(req.body);
      const message = await storage.createContactMessage(validatedData);
      res.json({
        message: "Contact message sent successfully",
        id: message.id
      });
    } catch (error) {
      if (error instanceof z.ZodError) {
        return res.status(400).json({ message: "Invalid form data", errors: error.errors });
      }
      console.error("Contact form error:", error);
      res.status(500).json({ message: "Failed to send message" });
    }
  });
  const httpServer = createServer(app2);
  return httpServer;
}

